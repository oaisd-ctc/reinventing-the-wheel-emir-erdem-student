        public static int Power(int x, int y)
        {
            ///<summary>
            ///Finds the result of the number raised to the nth power
            ///</summary>
            ///<param name ="x">The first number.</param>
            ///<param name ="y">The exponent.</param>
            ///<returns>
            ///Result of the formula x^y 
            ///</returns>
            int result = 1;
            if (y > 0)
            {
                for (int i = 0; i < y; i++)
                {
                    result *= x;
                }
            }
            if (y < 0)
            {
                for (int i = -1; i >= y; i--)
                {
                    result /= x;
                }
            }
            return result;
        }
