        public static decimal Sqrt(decimal number)
        {
            ///<summary>
            ///Finds the square root of the number
            ///</summary>
            ///<param name ="x">The input.</param>
            ///<returns>
            ///Returns the root
            ///</returns>
            decimal temp = 0;
            decimal sqrt = number / 2;

            while (sqrt != temp)
            {
                temp = sqrt;
                sqrt = (number / temp + temp) / 2;
            }
            return sqrt;
        }
